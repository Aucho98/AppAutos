function catalogo_x_marcas(string $marca): array{
    $resultado = [];
    $catalogo = catalogo_completo();

    foreach($catalogo AS $m){
        if($m['marcam'] == $marca){
            //agregar elemento al array
            $resultado[] = $m;
        }
    }

    return $resultado;
}


function producto_x_id($idAuto){

    $catalogo = catalogo_completo();
    
    foreach ($catalogo AS $m) {
        if ($m['id'] == $idAuto) {
            return $m;
        }
    }
    return null;
}

